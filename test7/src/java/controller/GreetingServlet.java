    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        processRequest(request, response);
    } 

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        HttpSession session = request.getSession();
        Account currentUser = (Account) session.getAttribute("account");

        if (currentUser == null) {
            response.sendRedirect("login.jsp");
            return;
        }

        String targetUserIdRaw = request.getParameter("userId");

        int targetUserId;
        try {
            targetUserId = Integer.parseInt(targetUserIdRaw);
        } catch (NumberFormatException e) {
            response.sendRedirect("home.jsp");
            return;
        }
        
        MessageDAO messDAO = new MessageDAO();
        boolean success = false;
        
        success = messDAO.sendMessage(currentUser.getUserId(), targetUserId, "ðŸ‘‹");
        
        System.out.println("Greeting successfully:"+ success);
        response.sendRedirect("user.jsp?userId=" + targetUserId);
    }

